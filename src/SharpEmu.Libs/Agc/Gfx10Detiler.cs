using System.Numerics;

namespace SharpEmu.Libs.Agc;

/// <summary>
/// CPU detiler for the GFX10/RDNA2 swizzle modes carried by Gen5 T# descriptors
/// (SQ_IMG_RSRC.SW_MODE): 5 = SW_4KB_S, 24 = SW_64KB_Z_X, 27 = SW_64KB_R_X.
/// Converts tiled guest surface bytes into row-major linear elements before the
/// Vulkan upload. These are NOT the GCN/GNM tile-mode indices (GNM has no mode
/// 27 at all); they index the GFX10 AddrSwizzleMode table.
///
/// Layout model (AMD addrlib, Gfx10Lib::ComputeSurfaceAddrFromCoordMacroTiled):
/// the surface is padded to whole 4KB/64KB swizzle blocks and the blocks are
/// stored row-major over the padded pitch. Within a block, byte-offset bit i is
/// the XOR of the element-coordinate bits selected by an (xMask, yMask) pair:
///   bit(i) = parity(x &amp; xMask[i]) ^ parity(y &amp; yMask[i])
/// evaluated on the global element coordinates. The low log2(bytesPerElement)
/// bits address the byte within the element, so one element's bytes stay
/// contiguous. The mask pairs are transcribed from the addrlib swizzle pattern
/// tables (Mesa src/amd/addrlib/src/gfx10/gfx10SwizzlePattern.h, nibble tables
/// composed per GFX10_SW_*_PATINFO); slice and sample terms drop out for the 2D
/// single-sample surfaces handled here.
///
/// The _X modes fold pipe-routing XOR terms into bits 8+ and therefore depend
/// on the GPU's pipe count. The PS5 pipe count is not publicly documented;
/// PipesLog2 defaults to 16 pipes (Oberon is Navi10-class: 256-bit GDDR6,
/// 16 RBs, and Navi10's GB_ADDR_CONFIG selects 16 pipes). All pipe variants are
/// tabled so the constant can be recalibrated against captured surfaces if
/// mode-27 content still shows block-local scrambling. The per-surface
/// pipeBankXor is taken as 0, which holds for 64KB-aligned AGC allocations.
/// </summary>
internal static class Gfx10Detiler
{
    internal const uint SwLinear = 0;
    internal const uint Sw4KbStandard = 5;
    internal const uint Sw64KbDepth = 24;
    internal const uint Sw64KbRender = 27;

    // log2 of the pipe count used to select the _X mode pattern; 4 = 16 pipes.
    private const int PipesLog2 = 4;

    private const int Block4KbSizeLog2 = 12;
    private const int Block64KbSizeLog2 = 16;
    private const int ElementSizeCount = 5;

    internal static bool IsSupportedTileMode(uint tileMode) =>
        tileMode is Sw4KbStandard or Sw64KbDepth or Sw64KbRender;

    /// <summary>
    /// Byte count of the tiled surface footprint: the pitch and height padded up
    /// to whole swizzle blocks. This exceeds width * height * bytesPerElement
    /// whenever the surface is not block-aligned (e.g. a 1280x720 32-bit
    /// SW_64KB_R_X surface occupies 1280x768 padded elements = 3,932,160 bytes).
    /// Returns 0 when the tile mode or element size is unsupported.
    /// </summary>
    internal static ulong GetTiledByteCount(
        uint tileMode,
        uint widthElements,
        uint heightElements,
        uint pitchElements,
        uint bytesPerElement)
    {
        if (!TryGetLayout(tileMode, bytesPerElement, out var layout))
        {
            return 0;
        }

        var pitch = Math.Max(pitchElements, widthElements);
        var blocksPerRow = (pitch + layout.BlockWidth - 1) / layout.BlockWidth;
        var blockRows = (heightElements + layout.BlockHeight - 1) / layout.BlockHeight;
        return (ulong)blocksPerRow * blockRows << layout.BlockSizeLog2;
    }

    /// <summary>
    /// Converts a tiled surface into tightly packed row-major elements
    /// (widthElements * heightElements * bytesPerElement bytes). Elements whose
    /// tiled offset falls outside the provided source (a short read near the end
    /// of a guest mapping) are left zero.
    /// </summary>
    internal static byte[] Detile(
        ReadOnlySpan<byte> tiled,
        uint tileMode,
        uint widthElements,
        uint heightElements,
        uint pitchElements,
        uint bytesPerElement)
    {
        if (!TryGetLayout(tileMode, bytesPerElement, out var layout))
        {
            throw new ArgumentException(
                $"Unsupported tile mode {tileMode} / element size {bytesPerElement}.");
        }

        var linear = new byte[checked((int)((ulong)widthElements * heightElements * bytesPerElement))];
        var pitch = Math.Max(pitchElements, widthElements);
        var blocksPerRow = (pitch + layout.BlockWidth - 1) / layout.BlockWidth;
        var bitCount = layout.BlockSizeLog2;
        var masks = layout.Masks;

        var columnOffsets = new uint[widthElements];
        for (var x = 0u; x < widthElements; x++)
        {
            var value = 0u;
            for (var bit = 0; bit < bitCount; bit++)
            {
                value |= (uint)(BitOperations.PopCount(x & masks[bit]) & 1) << bit;
            }

            columnOffsets[x] = value;
        }

        var rowOffsets = new uint[heightElements];
        for (var y = 0u; y < heightElements; y++)
        {
            var value = 0u;
            for (var bit = 0; bit < bitCount; bit++)
            {
                value |= (uint)(BitOperations.PopCount(y & masks[bitCount + bit]) & 1) << bit;
            }

            rowOffsets[y] = value;
        }

        var elementBytes = (int)bytesPerElement;
        for (var y = 0u; y < heightElements; y++)
        {
            var blockRowBase = (ulong)(y / layout.BlockHeight) * blocksPerRow;
            var rowOffset = rowOffsets[y];
            var destination = (long)y * widthElements * elementBytes;
            for (var x = 0u; x < widthElements; x++)
            {
                var tiledOffset = (long)(
                    (blockRowBase + x / layout.BlockWidth) << layout.BlockSizeLog2 |
                    (columnOffsets[x] ^ rowOffset));
                if (tiledOffset + elementBytes <= tiled.Length)
                {
                    tiled.Slice((int)tiledOffset, elementBytes)
                        .CopyTo(linear.AsSpan((int)destination, elementBytes));
                }

                destination += elementBytes;
            }
        }

        return linear;
    }

    /// <summary>
    /// Tiled byte offset of one element; the mapping the detile loop applies,
    /// exposed for tests.
    /// </summary>
    internal static ulong GetTiledByteOffset(
        uint tileMode,
        uint x,
        uint y,
        uint pitchElements,
        uint bytesPerElement)
    {
        if (!TryGetLayout(tileMode, bytesPerElement, out var layout))
        {
            throw new ArgumentException(
                $"Unsupported tile mode {tileMode} / element size {bytesPerElement}.");
        }

        var blocksPerRow = (pitchElements + layout.BlockWidth - 1) / layout.BlockWidth;
        var offset = 0u;
        for (var bit = 0; bit < layout.BlockSizeLog2; bit++)
        {
            var parity = (BitOperations.PopCount(x & layout.Masks[bit]) ^
                          BitOperations.PopCount(y & layout.Masks[layout.BlockSizeLog2 + bit])) & 1;
            offset |= (uint)parity << bit;
        }

        var blockIndex = (ulong)(y / layout.BlockHeight) * blocksPerRow + x / layout.BlockWidth;
        return blockIndex << layout.BlockSizeLog2 | offset;
    }

    private readonly record struct TileLayout(
        int BlockSizeLog2,
        uint BlockWidth,
        uint BlockHeight,
        ushort[] Masks);

    private static bool TryGetLayout(uint tileMode, uint bytesPerElement, out TileLayout layout)
    {
        layout = default;
        var elementSizeLog2 = bytesPerElement switch
        {
            1 => 0,
            2 => 1,
            4 => 2,
            8 => 3,
            16 => 4,
            _ => -1,
        };
        if (elementSizeLog2 < 0)
        {
            return false;
        }

        switch (tileMode)
        {
            case Sw4KbStandard:
                layout = new TileLayout(
                    Block4KbSizeLog2,
                    Block4KbWidths[elementSizeLog2],
                    Block4KbHeights[elementSizeLog2],
                    Sw4KbStandardMasks[elementSizeLog2]);
                return true;
            case Sw64KbDepth:
                layout = new TileLayout(
                    Block64KbSizeLog2,
                    Block64KbWidths[elementSizeLog2],
                    Block64KbHeights[elementSizeLog2],
                    Sw64KbDepthMasks[PipesLog2 * ElementSizeCount + elementSizeLog2]);
                return true;
            case Sw64KbRender:
                layout = new TileLayout(
                    Block64KbSizeLog2,
                    Block64KbWidths[elementSizeLog2],
                    Block64KbHeights[elementSizeLog2],
                    Sw64KbRenderMasks[PipesLog2 * ElementSizeCount + elementSizeLog2]);
                return true;
            default:
                return false;
        }
    }

